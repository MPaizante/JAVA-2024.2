package Basico;

public class Aula09EstruturaRepeticao {
    // while, do while , for...
    public static void main(String[] args) {
        int cout = 0;
        while (cout < 3){
            System.out.println("A ");
            cout++;
        }

        for (int i = 0; i < 3; i++) {
            System.out.println("B");
        }
    }
}
